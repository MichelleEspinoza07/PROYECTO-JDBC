    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
       
        FileReader fr=null;
        try {
            int nLineas=0;
            int i=0;
            String [] usuarios = null;
            String linea;
            sc= new Scanner (new File ("C:\\Users\\Master\\Desktop\\MICHELLE\\PROG. NETBEANS\\PrototipoJDBC\\usuarios.txt"));
            File f = new File ("C:/Users/Master/Desktop/MICHELLE/PROG. NETBEANS/PrototipoJDBC/usuarios.txt");
            fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            
            try {
                while ((linea=br.readLine())!=null){
                    nLineas++;
                }} catch (IOException ex) {
                Logger.getLogger(Frm_Ingreso.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            usuarios=new String [nLineas]; // el tamano del arreglo
            
            while (sc.hasNextLine()){
                usuarios[i++] = sc.nextLine();
            }
            intentos++;
            user = txtUsuario.getText();
            password = txtClave.getText();
            
            Seguridad s = new Seguridad();
            s.validarUsuario(usuarios, user, password, intentos);
               
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Frm_Ingreso.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Frm_Ingreso.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                fr.close();
            } catch (IOException ex) {
                Logger.getLogger(Frm_Ingreso.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (intentos==0)this.dispose(); // se descargue de memoria
    }//GEN-LAST:event_btnIngresarActionPerformed

