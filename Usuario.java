    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        // TODO add your handling code here:
        this.modelo.addRow(new Object[]{
            this.JTRCodigo.getText(),
            this.JTRApellido.getText(),
            this.JTRNombre.getText(),
            this.JTRAlia.getText(),
            this.JTRClave.getText(),
            this.JTREstado.getText(),
        });
        
        this.JTRCodigo.setText("");
        this.JTRApellido.setText("");
        this.JTRNombre.setText("");
        this.JTRAlia.setText("");
        this.JTRClave.setText("");
        this.JTREstado.setText("");
        JTRCodigo.grabFocus();
    }//GEN-LAST:event_BtnGuardarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        // TODO add your handling code here:
        int fila_seleccionada=JTablaUsuario.getSelectedRow();
        if(fila_seleccionada>=0){
            modelo.removeRow(fila_seleccionada);
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una fila por favor");
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

