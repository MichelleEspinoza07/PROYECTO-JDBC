    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        // TODO add your handling code here:
        String []datos=new String[6];
        datos[0]=JTRCodigo.getText();
        datos[0]=JTRApellido.getText();
        datos[0]=JTRNombre.getText();
        datos[0]=JTRAlia.getText();
        datos[0]=JTRClave.getText();
        datos[0]=JTREstado.getText();
        
        for(int i=0; i<JTablaUsuario.getColumnCount();i++){
            modelo.setValueAt(datos[i], filas, i);
        }
    }//GEN-LAST:event_BtnModificarActionPerformed

    private void JTablaUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTablaUsuarioMouseClicked
        // TODO add your handling code here:
        int fila_seleccionada=JTablaUsuario.getSelectedRow();
        JTRCodigo.setText(JTablaUsuario.getValueAt(fila_seleccionada, 0).toString());
        JTRApellido.setText(JTablaUsuario.getValueAt(fila_seleccionada, 1).toString());
        JTRNombre.setText(JTablaUsuario.getValueAt(fila_seleccionada, 2).toString());
        JTRAlia.setText(JTablaUsuario.getValueAt(fila_seleccionada, 3).toString());
        JTRClave.setText(JTablaUsuario.getValueAt(fila_seleccionada, 4).toString());
        JTREstado.setText(JTablaUsuario.getValueAt(fila_seleccionada, 5).toString());
        filas=fila_seleccionada;
    }//GEN-LAST:event_JTablaUsuarioMouseClicked

